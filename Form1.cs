        private void nameInput_KeyPress(object sender, KeyPressEventArgs e)
        {
            if (!IsHebrewCharacter(e.KeyChar) && !char.IsWhiteSpace(e.KeyChar) && e.KeyChar != (char)Keys.Back)
            {
                e.Handled = true; // Prevent the character from being entered.
            }
        }

        private bool IsHebrewCharacter(char character)
        {
            // The Unicode range for Hebrew characters is U+05D0 to U+05EA.
            // You can add more ranges or characters as needed.
            return (character >= 0x05D0 && character <= 0x05EA);
        }

        private void richTextBox1_KeyPress(object sender, KeyPressEventArgs e)
        {
