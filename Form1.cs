        private void printButton_Click(object sender, EventArgs e)
        {
            PrintDocument printDocument = new PrintDocument();
            printDocument.PrintPage += PrintDocument_PrintPage;

            PrintDialog printDialog = new PrintDialog();
            printDialog.Document = printDocument;

            if (printDialog.ShowDialog() == DialogResult.OK)
            {
                printDocument.Print();
            }
        }

        private void PrintDocument_PrintPage(object sender, PrintPageEventArgs e)
        {
            string textToPrint = richTextBox1.Text;
            Font font = richTextBox1.Font;

            e.Graphics.DrawString(textToPrint, font, Brushes.Black, 100, 100);
        }

        private void checkBox1_CheckedChanged(object sender, EventArgs e)
        {

            if (checkBox1.Checked)
            {
                kra = true;
                string word = "קרע שטן";
                foreach (char letter in word)
                {
                    string kra = getLetter(letter);
                    richTextBox1.AppendText(kra + Environment.NewLine + Environment.NewLine);
                }
            }

            else if (kra == true)
            {
                kra = false;

                if (richTextBox1.Lines.Length == 58)
                {
                    richTextBox1.Clear();
                }

                else if (richTextBox1.Lines.Length > 58)
                {
                    int lineCount = richTextBox1.Lines.Length;

                    int linesToKeep = lineCount - 58;

                    string[] lines = richTextBox1.Lines.Take(linesToKeep).ToArray();
                    richTextBox1.Lines = lines;

                    richTextBox1.AppendText(Environment.NewLine);

                }

